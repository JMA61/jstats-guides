  # ---- working-directory scrubbing (S184) ---------------------------------
  # jstats prints ABSOLUTE paths (jsave: "Saved X to <path>"; jload: "Found X
  # in <path>"), forward-slashed by .jst_norm_path(). Rendered as captured, a
  # box would publish the RENDER machine's directory tree -- which is not the
  # reader's, and nobody's business. Every captured path is rewritten to the
  # SAME placeholder the Console chrome strip already carries in RStudio's
  # link-blue: "~/your-working-directory/". Chrome and live output then agree,
  # which is what makes the placeholder a teaching device rather than a fudge.
  # A real path that survives the rewrite HALTS the render (publish gate) --
  # the same fail-loud principle as expect_error = FALSE.
  WD_PLACEHOLDER <- "~/your-working-directory"
  scrub_wd <- function(txt){
    if (!length(txt) || !nzchar(txt)) return(txt)
    wd <- getwd()
    norm <- function(w) tryCatch(normalizePath(wd, winslash = w, mustWork = FALSE),
                                 error = function(e) wd)
    cands <- unique(c(norm("/"), norm("\\"), gsub("\\\\", "/", wd),
                      gsub("/", "\\\\", wd), wd))
    cands <- cands[order(-nchar(cands))]      # longest first: no partial hits
    for (v in cands) txt <- gsub(v, WD_PLACEHOLDER, txt, fixed = TRUE)
    gsub("your-working-directory\\\\", "your-working-directory/", txt)
  }
  leak_check <- function(txt){
    if (grepl("(?<![A-Za-z])[A-Za-z]:[\\\\/]|/home/|/Users/|/root/", txt, perl = TRUE))
      stop("pane_facsimile: a real file path survived scrubbing -- this box would ",
           "publish the render machine's directory. Offending output:\n", txt,
           call. = FALSE)
    invisible(TRUE)
  }

  # ---- warning formatting (S187) -------------------------------------------
  # Real top-level R does NOT print "Warning: <msg>" inline. It DEFERS warnings
  # to the end of the top-level call (the default options(warn = 0)) and then
  # prints them under a header:
  #     Warning message:            <- one warning
  #     package 'jstats' was built under R version 4.5.3
  #     Warning message:            <- one warning raised inside a call
  #     In g() : with a call attached
  #     Warning messages:           <- several, numbered
  #     1: first
  #     2: second
  # Warnings are therefore collected in their OWN buffer, not interleaved into
  # msgbuf, so they land after any message()/packageStartupMessage() output no
  # matter which order the two were signalled in. Rendered with the message
  # class -- RStudio colors messages and warnings alike.
  fmt_warn <- function(w){
    cl <- conditionCall(w)
    if (is.null(cl)) conditionMessage(w)
    else paste0("In ", paste(deparse(cl), collapse = " "), " : ", conditionMessage(w))
  }
  warn_block <- function(ws){
    if (length(ws) == 1L) paste0("Warning message:\n", ws)
    else paste0("Warning messages:\n",
                paste0(seq_along(ws), ": ", ws, collapse = "\n"))
  }

  # ECHO FIDELITY (S189). The echo is built from the RAW SOURCE LINES a statement
  # spans -- src[first_line:last_line] -- NOT from as.character(srcref), which
  # honors the srcref's COLUMNS and so stops at the last token, silently dropping
  # any TRAILING comment ("mean(x)  # a poisoned mean" echoed as "> mean(x)").
  # Whole-line indexing makes the box show each command line AS TYPED, comment and
  # all, which is what RStudio does. STANDALONE comment lines still do not echo:
  # they are not expressions, so no srcref points at them -- deliberate, and
  # disclosed to the reader (data.qmd block 1's box lead and the page-wrap box).
  # KNOWN CAVEAT: two statements on one physical line, separated by ";", would
  # echo that line twice (one srcref each). No page does this; keep it that way.
  # ECHO OFF (S192): echo = FALSE drops the "> " prompt segment entirely, for
  # output that reaches the Console WITHOUT the reader typing anything -- the
  # model case is the .Rprofile startup message, which appears at session start
  # with no command line above it. The code still RUNS (same scrub, same leak
  # gate); only the echo is withheld. Default TRUE preserves every prior page.
  run_block <- function(code, expect_error = FALSE, echo = TRUE){
    exprs <- parse(text = code, keep.source = TRUE)
    refs  <- attr(exprs, "srcref")
    src   <- strsplit(code, "\n", fixed = TRUE)[[1]]
    segs  <- list()
    add   <- function(type, text){
      text <- scrub_wd(text)                  # no real paths reach a box
      if (nzchar(text)){
        leak_check(text)
        segs[[length(segs)+1L]] <<- list(type=type, text=text)
      }
    }
    for (i in seq_along(exprs)){
      if (isTRUE(echo)){
        srclines <- src[refs[[i]][1L]:refs[[i]][3L]]   # whole physical lines (see above)
        srclines[1] <- paste0("> ", srclines[1])
        if (length(srclines) > 1) srclines[-1] <- paste0("+ ", srclines[-1])
        add("prompt", paste(srclines, collapse = "\n"))
      }
      msgbuf <- character(0); warnbuf <- character(0); errbuf <- character(0)
      out <- capture.output(
        withCallingHandlers({
          ev <- if (expect_error)
                  tryCatch(withVisible(eval(exprs[[i]], globalenv())),
                           error = function(e){ errbuf[[length(errbuf)+1L]] <<- paste0("Error: ", conditionMessage(e)); list(visible=FALSE, value=NULL) })
                else withVisible(eval(exprs[[i]], globalenv()))
          if (isTRUE(ev$visible)) print(ev$value)
        },
        message = function(m){ msgbuf[[length(msgbuf)+1L]] <<- sub("\n$","",conditionMessage(m)); invokeRestart("muffleMessage") },
        warning = function(w){ warnbuf[[length(warnbuf)+1L]] <<- fmt_warn(w); invokeRestart("muffleWarning") })
      )
      add("stdout", paste(out, collapse = "\n"))
      if (length(msgbuf))  add("message", paste(msgbuf, collapse = "\n"))
      if (length(warnbuf)) add("message", warn_block(warnbuf))
      if (length(errbuf))  add("error",   paste(errbuf, collapse = "\n"))
    }
    segs
  }

